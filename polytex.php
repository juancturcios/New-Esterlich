                <h3>PAS 1075 </h3>
                <hr>
                <p>En términos de una definición común del material PE 100-RC de la PAS 1075 (Especificación Disponible Pública) titulada Tubos de Polietileno para las Tecnologías de Instalación Alternativas fue publicado por DIN. Esta especificación pública disponible es considerada un suplemento a las normas y regulaciones existentes.  El ámbito de aplicación de PAS 1075 es el aumento de la resistencia al crecimiento lento de grieta de tuberías de PE 100-RC que se utilizan para tecnologías de instalación alternativas, tales como perforación horizontal direccional, revestimiento o instalación sin campo de arena. Los requisitos, características y métodos de prueba, así como los procedimientos de control de calidad respectivos están regulados y se garantizarán mediante inspección de terceros.
